////////////////////////////////////////////////////////////////////////////////
//! \par	Description:
//!	Default constructor
//!
//! \throw	None.
//!
//! \returns	Not applicable.
//!
////////////////////////////////////////////////////////////////////////////////

RandomGenerator::RandomGenerator(void) noexcept: RandomSelector(),
	GeneratorArray()
	{
	register	std_uint	i;

	for (i = 0; i < number_of_elements(GeneratorArray); ++i)
		GeneratorArray [i].SetParameters(Data [i].Multiplier,
		Data [i].Constant, Data [i].Module);
	}

////////////////////////////////////////////////////////////////////////////////
//! \par	Description:
//!	Initialize the seeds of a random number generator.
//!
//! \param	SelectorSeed	Seeds for the RandomSelector generator.
//! \param	Seed		The array of seeds for the linear generators.
//!
//! \throw	None.
//!
//! \returns	None.
//!
//! \see	LINEAR_GENERATORS
//!
////////////////////////////////////////////////////////////////////////////////

void	RandomGenerator::SetSeeds(std_uint SelectorSeed,
		const std_uint Seed [LINEAR_GENERATORS]) noexcept
	{
	register	std_uint	i;

	RandomSelector.SetSeed(SelectorSeed);
	for (i = 0; i < number_of_elements(GeneratorArray); ++i)
		GeneratorArray [i].SetSeed(*Seed++);
	}

